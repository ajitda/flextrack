    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $inputData = $request->all();
        $validation = $this->productValidation($inputData);
        if ($validation->fails()) {
            return $this->sendError('Validation Error!', $validation->errors());
        }
        $product = new Product();
        $result = $product->saveProduct($inputData);
        if ($result['status']) {
            return $this->sendResponse($result['product'], 'Product Created Successfully!');
        } else {
            return $this->sendError('Product Can not be created!');
        }
    }

    protected function productValidation(array $data)
    {
        return Validator::make($data, [
            'name' => ['required', 'string', 'max:255'],
            'description' => ['required', 'string'],
            'price' => ['required', 'string'], 
        ]);
    }

